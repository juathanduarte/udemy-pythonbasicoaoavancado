# Ranges são utilizados para gerar sequência númericas de maneira especificada.
# range(valor_de_inicio, valor_de_fim, valor_de_passo)

# Forma 1

for num in range(11):
    print(num)

# Forma 2

for num in range(1, 11):
    print(num)

# Forma 3    

for num in range(1, 10, 2):
    print(num)

# Forma 5

for num in range(10, 0, -1):
    print(num)